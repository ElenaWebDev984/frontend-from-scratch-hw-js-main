function capitalizeWords(str) {
  // –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Å—Ç—Ä–æ–∫—É –≤ –º–∞—Å—Å–∏–≤ —Å–∏–º–≤–æ–ª–æ–≤
  let chars = str.split('');

  // –î–µ–ª–∞–µ–º –ø–µ—Ä–≤—É—é –±—É–∫–≤—É –∑–∞–≥–ª–∞–≤–Ω–æ–π
  if (chars.length > 0) {
      chars[0] = chars[0].toUpperCase();
  }

  // –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –º–∞—Å—Å–∏–≤—É —Å–∏–º–≤–æ–ª–æ–≤ –∏ –¥–µ–ª–∞–µ–º –∑–∞–≥–ª–∞–≤–Ω—ã–º–∏ –ø–µ—Ä–≤—ã–µ –±—É–∫–≤—ã –∫–∞–∂–¥–æ–≥–æ —Å–ª–æ–≤–∞
  for (let i = 1; i < chars.length; i++) {
      if (chars[i - 1] === ' ') {
          chars[i] = chars[i].toUpperCase();
      } else {
          chars[i] = chars[i].toLowerCase();
      }
  }

  // –û–±—ä–µ–¥–∏–Ω—è–µ–º –º–∞—Å—Å–∏–≤ —Å–∏–º–≤–æ–ª–æ–≤ –æ–±—Ä–∞—Ç–Ω–æ –≤ —Å—Ç—Ä–æ–∫—É
  return chars.join('');
}

console.log(capitalizeWords("hello world from javascript")); // "Hello World From JavaScript"
